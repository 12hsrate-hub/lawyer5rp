def _build_focus_input_section(*, complaint_basis: str, main_focus: str) -> str:
    normalized_basis = (complaint_basis or "").strip()
    normalized_focus = (main_focus or "").strip()
    if not normalized_basis and not normalized_focus:
        return ""

    basis_line = BASIS_LABELS.get(normalized_basis, normalized_basis or "не указано")
    explanation = BASIS_EXPLANATIONS.get(normalized_basis, "")
    parts = [
        f"Основание жалобы: {basis_line}",
        f"Основной акцент: {normalized_focus or 'не указан'}",
    ]
    if explanation:
        parts.append(f"Смысл выбранного основания: {explanation}")
    parts.append("Используй эти поля только как ориентир для акцента, не подменяя ими факты черновика.")
    return "\n".join(parts)


def _build_selected_basis_sections(*, complaint_basis: str) -> tuple[str, str]:
    normalized_basis = (complaint_basis or "").strip()
    if not normalized_basis:
        return (DEFAULT_LEGAL_POINTS, DEFAULT_STRATEGY)
    return (
        BASIS_LEGAL_POINTS.get(normalized_basis, ""),
        BASIS_STRATEGIES.get(normalized_basis, ""),
    )


