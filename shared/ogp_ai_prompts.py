class PromptSpec:
    name: str
    version: str
    text: str


def _render_prompt_sections(*sections: tuple[str, str]) -> str:
    parts: list[str] = []
    for title, body in sections:
        normalized = body.strip()
        if normalized:
            parts.append(f"[{title}]\n{normalized}")
    return "\n\n".join(parts).strip()


